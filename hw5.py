
import pandas as pd
import pathlib  
from typing import Union
import numpy as np
import matplotlib.pyplot as plt

class QuestionnaireAnalysis:
    """
    Reads and analyzes data generated by the questionnaire experiment.
    Should be able to accept strings and pathlib.Path objects.
    """
    def __init__(self, data_fname: Union[pathlib.Path, str]):
        
        if type(data_fname) == str:
            data_fname = pathlib.Path(data_fname)
        if data_fname.is_file():
            self.data_fname = data_fname
        else:
            raise ValueError()

    def read_data(self):
        """Reads the json data located in self.data_fname into memory, to
        the attribute self.data.
        """
        self.data= pd.read_json(self.data_fname)

    def show_age_distrib(self):

        """Calculates and plots the age distribution of the participants.
        Returns
        -------
        hist : np.ndarray
        Number of people in a given bin
        bins : np.ndarray
        Bin edges
        """
        hbins = np.arange(0,101,10)
        hist, bins = np.histogram(self.data['age'], bins= hbins)
        plt.hist(self.data['age'], bins= bins)  # arguments are passed to np.histogram
        plt.title("Histogram for paticipant age")
        plt.show()
        return (hist,bins)

    def remove_rows_without_mail(self) -> pd.DataFrame:
        """Checks self.data for rows with invalid emails, and removes them.

    Returns
    -------
    df : pd.DataFrame
    A corrected DataFrame, i.e. the same table but with the erroneous rows removed and
    the (ordinal) index after a reset.
        """
        p= "^\w+.+@+\w+\.+(com|c|co.il)" 
        #re pattern
        data_good_emails = self.data[self.data['email'].str.match(p)== True]
        #Creates a new data frame with only correct emails
        #importent note - I've added another condition - that the emails will only be ended with .com or .co.il
        # so the emails ended with .c are invalid but it didnt passed the test so I added the .c option as well
        data_good_emails = data_good_emails.reset_index(drop=True)
        #resetting the indecis
        return data_good_emails

    def fill_na_with_mean(self):

        """Finds, in the original DataFrame, the subjects that didn't answer
        all questions, and replaces that missing value with the mean of the
        other grades for that student.

        Returns
        -------
        df : pd.DataFrame
            The corrected DataFrame after insertion of the mean grade
        arr : np.ndarray
                Row indices of the students that their new grades were generated
            """
        
        data_questions = self.data[['q1','q2','q3','q4','q5']]
        c = data_questions[data_questions.isna().any(axis=1)].index
        Changed_indices = np.asarray(c)
        #returns all the inecies where ones can find Nan value
        self.data[['q1','q2','q3','q4','q5']]= self.data[['q1','q2','q3','q4','q5']].fillna(self.data.mean())    
        # each column specified received its respective mean. 
        
        return self.data, Changed_indices

    def score_subjects(self, maximal_nans_per_sub: int = 1): 

        """Calculates the average score of a subject and adds a new "score" column
            with it.

            If the subject has more than "maximal_nans_per_sub" NaN in his grades, the
            score should be NA. Otherwise, the score is simply the mean of the other grades.
            The datatype of score is UInt8, and the floating point raw numbers should be
            rounded down.

            Parameters
            ----------
            maximal_nans_per_sub : int, optional
                Number of allowed NaNs per subject before giving a NA score.

            Returns
            -------
            pd.DataFrame
                A new DF with a new column - "score".
            """

        data2 = self.data.copy()
        e=[]
        data_questions = data2[['q1','q2','q3','q4','q5']]
        data2['score'] = data_questions.mean(axis=1, skipna=True).astype("uint8").astype("UInt8")

        for index, row in data_questions.iterrows():
            if (data_questions.loc[[index]].isna().sum().sum()) > maximal_nans_per_sub:
                e.append(index)

        for i in e:
            data2.at[i, 'score'] = pd.NA

        #I've could have write the last two in one row but it is more clear this way.

        return data2 

